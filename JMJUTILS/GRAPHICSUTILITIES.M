GraphicsUtilities::usage = "GraphicsUtilities.m is a package of graphics utilities."

scaleTicks::usage=
    "scaleTicks[p1,fac] returns the Graphics object p1 with all x and y tick \
marks scaled by factor fac.\nscaleTicks[p1,xfac,yfac] uses separate factors \
on x and y axes.\nscaleTicks[p1,{xfacp,xfacm},yfac] gives positive and \
negative x-tick marks scaled from the original positive ones, etc.";



