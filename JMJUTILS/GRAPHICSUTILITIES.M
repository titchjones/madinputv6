Begin["`Private`"]





protected = Unprotect[ Sin, Cos ]





Aux[f_] := Do[something]



staticvar = 0





Clear[scaleTicks];

scaleTicks[p1_Graphics,{xtp_,xtm_},{ytp_,ytm_}]:=
    Module[{ticksold,frameticksold,xtnew,ytnew,xftnew,yftnew},
        ticksold=Ticks/.AbsoluteOptions[p1];
        frameticksold=FrameTicks/.AbsoluteOptions[p1];
        xtnew=scaleTicks[First[ticksold],xtp,xtm];
        ytnew=scaleTicks[Last[ticksold],ytp,ytm];
        xftnew=scaleTicks[First[frameticksold],xtp,xtm];
        yftnew=scaleTicks[Last[frameticksold],ytp,ytm];
        (p1 /.Rule[Ticks,_]\[Rule]Rule[Ticks,{xtnew,ytnew}])
          /.Rule[FrameTicks,_]\[Rule]Rule[FrameTicks,{xftnew,yftnew}]
        ]/;And@@NonNegative/@{xtp,xtm,ytp,ytm};

scaleTicks[p1_Graphics,xt_,yt_]:=
    scaleTicks[p1,{xt,0},{yt,0}]/;And@@NonNegative/@{xt,yt};

scaleTicks[p1_Graphics,fac_]:=
    scaleTicks[p1,{fac,0},{fac,0}]/;NonNegative[fac];

scaleTicks[p1_Graphics,{xtp_,xtm_},yt_]:=
    scaleTicks[p1,{xtp,xtm},{yt,0}]/;And@@NonNegative/@{xtp,xtm,yt};

scaleTicks[p1_Graphics,xt_,{ytp_,ytm_}]:=
    scaleTicks[p1,{xt,0},{ytp,ytm}]/;And@@NonNegative/@{xt,ytp,ytm};

scaleTicks[ticks_List,facp_,facm_]:=
    ticks/.{x_,y_,{lp_,lm_},f_}\[Rule]{x,y,{facp,facm}lp,f};





