BeginPackage["Madtomma`Mfs`MatrixTable`",
	{"Madtomma`Mfs`Mfs`",
	"Madtomma`Algebra`Symplectic"}]



MatrixTable::usage = "MatrixTable.m is a package for working with MATRIX tables of transfer matrices saved by MAD (Version 9).";

makeMatrixTable::usage = "makeMatrixTable[mfsdata] transforms an mfs data object created from a MAD Version 9 MATRIX table into a MatrixTable data object.";



makeMatrixTable::badarg = "Wrong input mfs object.";







Begin["`Private`"]





protected =
  Unprotect[mfsTypes,mfsKeyValue,mfsKeyNames,mfsColumnNames,mfsInterpretKeys,
    mfsAddKey,mfsDeleteKey,mfsEditKey,mfsColumn];









orbtemplate = {"XC","PXC","YC","PYC","TC","PTC"};

mattemplate=Table["R" <> ToString[i] <> ToString[j]  ,{i,1,6},{j,1,6}];

orbpos[qpmfs_mfs]:=
  Flatten[(Position[
            mfsColumnNames[((StringReplace[#,{"CO"\[Rule]"C"}]&)/@
                  qpmfs)],#])&/@orbtemplate];

eigpos[qpmfs_mfs]:=
  Flatten[(Position[mfsColumnNames[qpmfs],#])&/@ Flatten[mattemplate]];







makeMatrixTable[qpmfs_mfs]:=Module[{},
		If[mfsKeyValue[qpmfs,"TYPE"]=="MATRIX",
			(
				MatrixTable[
						First[qpmfs],
					Join[
						Select[((StringReplace[#,{"CO"\[Rule]"C"}]&)/@
                  qpmfs\[LeftDoubleBracket]2\[RightDoubleBracket]),
							Not[MemberQ[Flatten[mattemplate\[Union] orbtemplate],#]]&
								],
						{"ORBIT","RMATRIX"}
						],
					({
					#\[LeftDoubleBracket]1\[RightDoubleBracket],
						#\[LeftDoubleBracket]2\[RightDoubleBracket],
						#\[LeftDoubleBracket]orbpos[qpmfs]\[RightDoubleBracket],
						Partition[#\[LeftDoubleBracket]eigpos[qpmfs]\[RightDoubleBracket],6]
									}&)/@Last[qpmfs]
						]
				),
			(Message[makeMatrixTable::badarg];Null)
		]
		];





mfsTypes=Union[mfsTypes,{MatrixTable}];



mfsKeyValue[qp_MatrixTable,keyss_]:=mfsKeyValue[mfs @@ qp,keyss]

mfsKeyNames[qp_MatrixTable]:=mfsKeyNames[mfs @@ qp]

mfsColumnNames[qp_MatrixTable]:=mfsColumnNames[mfs@@qp]

mfsInterpretKeys[qp_MatrixTable]:=mfsInterpretKeys[mfs @@ qp]

mfsAddKey[qp_MatrixTable,{key_String,value_}]:=
	mfsAddKey[mfs @@ qp,{key,value}]

mfsDeleteKey[qp_MatrixTable,key_String]:=mfsDeleteKey[mfs@@qp,key]

mfsEditKey[qp_MatrixTable,{key_String,newValue_}]:=
  mfsEditKey[mfs@@qp,{key,newValue}]

mfsColumn[qp_MatrixTable,key_String]:=
  Flatten[(#[[Flatten[Position[mfsColumnNames[qp],key]]]])& /@Last[qp],1]

mfsColumn[qp_MatrixTable,keys_List]:= mfsColumn[qp,#]& /@ keys

mfsColumn[qp_MatrixTable]:=Last[qp]



Protect[ Evaluate[protected] ];



End[ ]









Protect[makeMatrixTable]





EndPackage[ ]





































































































































































































































































































