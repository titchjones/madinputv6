elementIntervals[seq_mfs]:=
  N[Transpose[{mfsColumn[seq,"NAME"],
        mfsColumn[seq,"S"]-(mfsColumn[seq,"L"])/2,
        mfsColumn[seq,"S"]+(mfsColumn[seq,"L"])/2}],10];

findElement[seqints_List,s_/;NumericQ[s]]:=
  Select[seqints,(#[[2]]<s && s<#[[3]])&]

findElement[seq_mfs,s_/;NumericQ[s]]:=findElement[elementIntervals[seq],s]

findElement[seq_mfs,ss_List]:=Module[{seqints},
		seqints=elementIntervals[seq];
		Map[findElement[seqints,#]&,ss]]

findElement[seq_mfs,x_]:={{}}

\!\(\(splitElement[seqname_String, {el_String, s1_Real, s2_Real}, 
      s_ /; NumericQ[s]] := 
    Module[{el1 = el<>"\<.1\>", el2 = el<>"\<.2\>", def1, def2}, \n
      \t\t{MADnewElement[el1, {el, "\<L\>" \[Rule] \((s - s1)\)}], \n\t\t\t
        MADnewElement[el2, {el, "\<L\>" \[Rule] \((s2 - s)\)}], \n\t\t\t
        MADseqedit[seqname, \n
          \t\t\t{MADinstall[el1, "\<AT\>" \[Rule] \((s + s1)\)\/2 - s, 
              "\<FROM\>" \[Rule] el], \n\t\t\t\t
            MADinstall[el2, "\<AT\>" \[Rule] \((s + s2)\)\/2 - s, 
              "\<FROM\>" \[Rule] el], \n\t\t\t\tMADremove[el]}\n\t\t]\n\t\t}\n
      \t]\n\t\t\t\)\)

splitElement[seqname_String,seq_mfs,s_List/;And@@(NumericQ/@s)]:=(
    splitElement[seqname,seq,#]&/@ s)

splitElement[seqname_String,seq_mfs,s_/;NumericQ[s]]:=Module[{elint},
		elint=findElement[seq,s];
		If[elint=={},{},splitElement[seqname,First[elint],s]]
	]

splitElement[_,{},s_]:={}

splitElement[seqname_String,seq_,x_]:=
  MADcomment["Invalid azimuth given to splitElement"]





